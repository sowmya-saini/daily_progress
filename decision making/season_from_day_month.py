def season_day_month():
    month = input()
    day = int(input())
    if month == "March" and day == 20:
        print("Spring")
    elif  month == "June" and day == 21:
        print("Summer")
    elif month == "September" and day == 22:
        print("Fall")
    elif  month == "March" and day == 20:
        print("Winter")
season_day_month()
        